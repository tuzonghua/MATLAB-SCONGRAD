function [f,g] = scongrad_func7(x)

n = length(x);
g = zeros(n,1);

		f = 0;
		j = 1;         
		
		for i=1:n/2 
			t1 = x(2*i-1) - x(2*i)*(2-x(2*i)*(5-x(2*i)))-13;
			t2 = x(2*i-1) - x(2*i)*(14-x(2*i)*(1+x(2*i)))-29;
			
			f = f + t1*t1 + t2*t2;
			
			g(j)   = 2*(t1+t2);   
			g(j+1) = 2*t1*(10*x(2*i)-3*x(2*i)*x(2*i)-2)+...
		     		 2*t2*(3*x(2*i)*x(2*i)+2*x(2*i)-14);   
			j = j+2;        
		end      

end