function [f,g] = scongrad_func6(x)

n = length(x);
g = zeros(n,1);

		f = 0;

		for i=1:n/2
			f = f + (x(2*i-1)-2).^2 + ((x(2*i-1)-2).^2)*(x(2*i).^2) + (x(2*i)+1).^2;
		end   
		       
		j = 1;
		
		for i=1:n/2
			g(j)   = 2*(x(2*i-1)-2) + 2*(x(2*i-1)-2)*x(2*i)*x(2*i);
			g(j+1) = ((x(2*i-1)-2).^2)*2*x(2*i) + 2*(x(2*i)+1);
			j      = j+2;
		end

end