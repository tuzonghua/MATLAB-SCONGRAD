function [f,g] = scongrad_func10(x)

n = length(x);
g = zeros(n,1);

		c = 100;
		
		f=(x(1)-1).^2 + c*(x(1)-x(1).^2).^2;
		
		for i=2:n
			f = f + c*(x(1)-x(i).^2).^2;
		end
		
		g(1) = 2*(x(1)-1) + 2*c*(x(1)-x(1).^2)*(1-2*x(1));        
		
		for i=2:n
			g(1) = g(1) + 2*c*(x(1)-x(i).^2);
		end  
		
		for i=2:n
			g(i) = -4*c*x(i)*(x(1)-x(i).^2);
		end    
		
end